/*Ранее я говорил, что тип double называется типом двойной точности, и что он в некотором смысле лучше, чем тип float. 
Запустите следующую программу, чтобы убедиться в этом.*/

#include <stdio.h>
int main(void)
{
  int z = 1/10;
  float x = 1.0/10.0;
  double y = 1.0/10.0;
  
  printf("int:\n%d\n\n", z);
  printf("float:\n4\t--\t%.4f\n10\t--\t%.12f\n16\t--\t%.16f\n\n", x,x,x);
  printf("double:\n4\t--\t%.4f\n10\t--\t%.12f\n16\t--\t%.16f\n", y,y,y);
  
  return(0);
}

 /*Причина подобного результата в том, что количество памяти выделенное для хранения любой переменной ограничено.
 Из-за этого вещественные числа хранятся в памяти компьютера приближенно. 
 Для типа float памяти выделяется обычно меньше, чем для типа double. Поэтому возникают такие забавные моменты. 
 Отдельно обратите внимание на результат деления целых чисел, записанный в переменную z.*/